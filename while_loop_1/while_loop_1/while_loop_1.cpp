//

#include <iostream>
using namespace std;
int main()
{

	int i = 0;

	while (i < 5) {
		cout << i << "\n";
		i++;
     }  


	// do while 
	int index = 6;

	do
	{
		index++;
		cout << index << "\n";
	} while (index < 6);





}

